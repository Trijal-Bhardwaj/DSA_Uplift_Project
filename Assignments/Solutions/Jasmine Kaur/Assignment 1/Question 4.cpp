#include<iostream>
  using namespace std;

  int main()
  {

  int i,j,k;
   for(i=0;i<2;i++)
   {
       for(j=0;j<3-i;j++)
       {
           cout<<" ";
       }
       for(k=0;k<=i;k++)
       {
           cout<<"*"<<" ";
       }

       cout<<endl;
   }
   for(i=3;i>=0;i--)
   {
       for(j=0;j<=3-i;j++)
       {
           cout<<" ";
       }
       for(k=0;k<i;k++)
       {
           cout<<"*"<<" ";
       }

       cout<<endl;
   }
  }
